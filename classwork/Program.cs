    //int imin = 0, jmin = 0;
    for (int i = 0; i < matrix.GetLength(0); i++)
    {
        for (int j = 0; j < matrix.GetLength(1); j++)
        {
            int? g = Array.Find(uniq, x => x == matrix[i, j]);
            //Console.Write($"{g}");
            if (g == 0) {uniq.Append(matrix[i, j]);}
        }
    }
